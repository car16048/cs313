				<h2>Overview</h2>
				<div>
					<p>Soccer in Corvallis is comprised of three main organizations.  First, the local sports park offers indoor soccer programs for all ages.  Next, the local club has a few teams for each youth age between 8 and 18.  Finally, the national organization, the American Youth Soccer Organization (AYSO), offers soccer for ages 4 - 18, both in less competative and more competative programs.  This page focuses on AYSO.</p>
					<p>AYSO is an soccer organization with local units in almost all 50 states.  It operates as a single, national club, and as such is the largest soccer club in the world with over 500,000 youth.  Soccer is offered as a way to promote youth development, rather than as a means to a professional career.  As a result, the program is generally considered less competative than traditional soccer clubs.  Instead the focus is on what is called the Six Philosophies, statements or ideas meant to convey the spirit of the program, which is the building of youth through the means of soccer.</p>
				</div>
				<h2>Six Philosophies</h2>
				<div>
					<p>As listed on their <a href="http://www.ayso.org">national website</a>, AYSO's Philosophies are living tenets that separate it from other sports organizations. They are as follows.</p>
					<div id="accordion2">
						<h3>Everyone PlaysÂ®</h3>
						<div>
							<p>Our program's goal is for kids to play soccer so we mandate that every player on every team must play at least 50 percent of every game. It's no fun to spend the game on the bench...and that's no way to learn soccer!</p>
						</div>
						<h3>Balanced Teams</h3>
						<div>
							<p>Each year we form new teams as evenly balanced as possible because it's more fun and a better learning experience when teams of similar ability play. It allows for each player to gain the experience of a wide variety of teammates of different skill levels.</p>
						</div>
						<h3>Open Registration</h3>
						<div>
							<p>Our program is open to all children who want to register and play soccer. Interest and enthusiasm are the only criteria for playing. There are no elimination try-outs and nobody gets cut.</p>
						</div>
						<h3>Positive Coaching</h3>
						<div>
							<p>Encouragement of player effort provides for greater enjoyment for the players and leads to better-skilled and better-motivated players. A coach can be one of the most influential people in a child's life, so AYSO requires they create a positive experience for every boy and girl.</p>
						</div>
						<h3>Good Sportsmanship</h3>
						<div>
							<p>We strive to create a positive environment based on mutual respect rather than a win-at-all-costs attitude, and our program is designed to instill good sportsmanship in every facet of AYSO.</p>
						</div>
						<h3>Player Development</h3>
						<div>
							<p>We believe that all players should be able to develop their soccer skills and knowledge to the best of their abilities, both individually and as members of a team, in order to maximize their enjoyment of the game.</p>
						</div>
					</div>
				</div>
				<h2>Local Program</h2>
				<div>
					<p>AYSO's local programs are run by units called Regions.  These regions are grouped into Areas, and the Areas into Sections.  The AYSO National program contains 13 sections across the United States.  The region in Corvallis is Region 149, in Area S, in Section 2.  It has a locally run website at <a href="http://www.corvallisayso.org">corvallisayso.org</a>.  As of April 2017, the commissioner of Region 149 is Luke Cotton.</p>
